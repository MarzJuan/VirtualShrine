            <p></p>
            <p class="about-des">Museum of Philippine Political History (Museo ng Kasaysayang Pampulitika ng 
                Pilipinas),formerly located at the ground floor of the National Historical Commission of the 
                Philippines (NHCP) Building in T. M. Kalaw Street, Ermita, City of Manila, is now in its new 
                home, the Malolos Casa Real. This was formerly constructed as a school for women, but it is now 
                a historical museum that houses artifacts and memorabilia from the past. This structure played 
                a gigantic role in Philippine Republic.
            </p>
            <p class="about-des"> It traces the evolution of the Filipino nation-state and the emergence of its 
                system of government and democratic traditions amid the backdrop of long struggles for 
                independence. Miniature dioramas, theatrical backdrops, interactive terminals and original 
                artifacts recreate these periods, from the earliest communities to the Spanish and American 
                colonial periods through the Second World War until the present. Highlighted are major events 
                and figures in the country's political history.
            </p><p class="about-des"> As we move through time in the current day, the Casa Real Shrine has kept 
