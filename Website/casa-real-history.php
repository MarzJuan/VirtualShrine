                        <h2>Casa Real Shrine of Malolos</h2>

                        <div class="image-about">
                        <img src="Images/about/old-crs.jpg" alt="Casa Real Shrine">
                        </div>
                        <p>The Casa Real Shrine was built in 1580 and was stablished in 1767 – 1768 as the First Government Structure at Malolos.  Casa Real served as the House of Tribunal, Municipal House of Tribunal, Hall of Justice and Municipal Hall at different occasions. This also served as the the residence and office of the Gobernadorcillo during the Spanish time and office of those who were elected as Governors of Malolos from 1868 to 1892.</p>
                        <p>The building had witnessed the war between Filipinos and the Spaniards in 1896, and in 1898-1899 the patriotic struggle of Filipinos that were documented through La Independencia and Heraldo de La Revolucion that were printed by the National Printing Press that was maintained inside the building through a decree that was issued by the Congress of Malolos. A part of this building was also used as office of the National Treasury (1898-1899).</p>
                        <p>In 1965, the building was demolished to give way to a commercial project that was built in the same place.  However, the significance of this building as part of the golden history of Malolos was maintained when President Diosdado Macapagal declared the Casa Real a historic shrine, on October 4, 1965 through Presidential Decree No. 173.</p>
                        <p>This printing press was used from 1898 to 1899 under the press director, Zacarias Fajardo. After the end of the war against the United States, Bulacan revolutionary, Mariano Crisostomo took care of the printing press in his Imprenta Plaridel in Malolos</p>
                        <p>And in 1997 the government purchased the printing press and brought it back on its former home to remind the youth of the humble beginnings of our nation. This press stands as one of the symbols of our nation’s quest of freedom---the core of our pollical history.</p>
                        <p>At present, the building which was restored to its original form and is under the management of the National Historical Institute and serves as the final repository of still existing memorabilia. This structure played a gigantic role in Philippine Republic.  One of the permanent exhibits of Casa Real Shrine are relics of the Brave Women of Malolos of whom Rizal wrote a poem to honor their courage of setting up a school against objections from the towns friars.</p>
